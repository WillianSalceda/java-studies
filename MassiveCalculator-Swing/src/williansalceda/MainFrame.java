    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // TODO add your handling code here:
        
        int num = Integer.parseInt(txtNumberInserted.getValue().toString());
        int division = num / 2;
        int remainder = num % 2;
        double squareRoot = Math.sqrt(num);
        double cubicRoot = Math.cbrt(num);
        int absoluteValue = Math.abs(num);
        
        txtDivision.setText(Integer.toString(division));
        txtRemainder.setText(Integer.toString(remainder));
        txtSquareRoot.setText(Double.toString(squareRoot));
        txtCubicRoot.setText(Double.toString(cubicRoot));
        txtAbsolutValue.setText(Integer.toString(absoluteValue));
        
        FrameCalc.setVisible(true);
        
        
        
    }//GEN-LAST:event_btnCalculateActionPerformed

